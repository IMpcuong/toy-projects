template <typename T>
inline T nxt()
{
  T x;
  cin >> x;
  return x;
}

void solve()
{
  auto n = nxt<int>();
  vector<ll> scores(n);
  ranges::generate(scores, nxt<ll>);
  ranges::sort(scores);
  ranges::reverse(scores);

  int odds = ranges::count_if(scores, [](const ll &score) { return score & 1; });
  int evens = n - odds;

  ll a_score = 0;
  ll b_score = 0;
  for (int i = 0; i < n; i++)
  {
    if (i % 2 == 0 && scores[i] % 2 == 0)
      a_score += scores[i];

    if (i % 2 != 0 && scores[i] % 2 != 0)
      b_score += scores[i];
  }

  if (a_score == b_score)
  {
    println(_T);
    return;
  }

  a_score > b_score ? println(_A) : println(_B);
}

