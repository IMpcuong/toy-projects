const string _Y = "YES";
const string _N = "NO";
const string _TW = "2020";

inline ll gcd(ll a, ll b) { return b == 0 ? a : gcd(b, a % b); }
inline ll lcm(ll a, ll b) { return a / gcd(a, b) * b; }

template <typename T>
inline T nxt()
{
  T x;
  cin >> x;
  return x;
}

void solve()
{
  auto n = nxt<int>();
  auto s = nxt<string>();

  string first_four_chars = s.substr(0, 4);
  string last_four_chars = s.substr(n - 4, 4);
  if (first_four_chars == _TW || last_four_chars == _TW)
  {
    println(_Y);
    return;
  }

  string first_two_chars = first_four_chars.substr(0, 2);
  string last_two_chars = last_four_chars.substr(2, 2);
  if (first_two_chars + last_two_chars == _TW)
  {
    println(_Y);
    return;
  }

  string first_char = first_four_chars.substr(0, 1);
  string last_three_chars = last_four_chars.substr(1, 3);
  if (first_char + last_three_chars == _TW)
  {
    println(_Y);
    return;
  }

  string first_three_chars = first_four_chars.substr(0, 3);
  string last_char = last_four_chars.substr(3, 4);
  if (first_three_chars + last_char == _TW)
  {
    println(_Y);
    return;
  }

  println(_N);
}

